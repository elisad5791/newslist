CREATE DATABASE IF NOT EXISTS news CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE news;

CREATE TABLE categories ( 
	id INT PRIMARY KEY AUTO_INCREMENT,
	title VARCHAR(255) NOT NULL
) ENGINE=InnoDB;

CREATE TABLE news ( 
	id INT PRIMARY KEY AUTO_INCREMENT,
	title VARCHAR(255) NOT NULL,
	content TEXT NOT NULL,
	category_id INT,
	created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	CONSTRAINT news_category_id_fkey FOREIGN KEY (category_id) REFERENCES categories(id)
) ENGINE=InnoDB;

CREATE TABLE tags ( 
	id INT PRIMARY KEY AUTO_INCREMENT,
	title VARCHAR(255) NOT NULL
) ENGINE=InnoDB;

CREATE TABLE news_tags ( 
	id INT PRIMARY KEY AUTO_INCREMENT,
	tag_id INT,
	news_id INT,
    CONSTRAINT news_tags_tag_id_fkey FOREIGN KEY (tag_id) REFERENCES tags(id),
    CONSTRAINT news_tags_news_id_fkey FOREIGN KEY (news_id) REFERENCES news(id),
    CONSTRAINT unique_tag_news UNIQUE (tag_id, news_id)
) ENGINE=InnoDB;

CREATE TABLE users ( 
	id INT PRIMARY KEY AUTO_INCREMENT,
	first_name VARCHAR(255) NOT NULL,
	last_name VARCHAR(255) NOT NULL,
	email VARCHAR(255) NOT NULL
) ENGINE=InnoDB;

CREATE TABLE users_likes ( 
	id INT PRIMARY KEY AUTO_INCREMENT,
	user_id INT,
	news_id INT,
    CONSTRAINT users_likes_user_id_fkey FOREIGN KEY (user_id) REFERENCES users(id),
    CONSTRAINT users_likes_news_id_fkey FOREIGN KEY (news_id) REFERENCES news(id),
    CONSTRAINT unique_user_news UNIQUE (user_id, news_id)
) ENGINE=InnoDB;

CREATE TABLE pages_views ( 
	id INT PRIMARY KEY AUTO_INCREMENT,
	page_id INT,
	views INT,
    CONSTRAINT unique_page UNIQUE (page_id)
) ENGINE=InnoDB;

